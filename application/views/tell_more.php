American corner is conducted with the support of American Embassy of Sri Lanka. It provides free services to the community such as free courses, charity events etc. Currently there are four branches all around Sri Lanka. Such as American Corner (AC) Matara, American Corner (AC) Kandy, American Corner (AC) Colombo and American Corner (AC) Jaffna. 

The American Corner (AC) Matara builds bridges between the people of the United States and Sri Lanka through various programs and events.  Equipped with computers, Wi-Fi and other modern technology, and information about the United States, AC Matara partners with American and local organizations to host innovative programs that are free and open to the public. 

The American Corner (AC) Matara center is accessed by several kind of users. One of them are YF (Youth Forum) members and they are working as volunteers for the center. Each YF member should do volunteer service six hours per week. Other than YF members, center is used by kids, visitors, club members and library users. As well the Center is conducting free of charge courses and several kinds of clubs. Any person can participate for those courses including YF members.

As well there is an event definitely held in each month. That is coordinated by coordinator and budget proposal should send to the American Embassy. Allocated budget is handled by the YF members and they should produce a report of the expenditure after the event. The YF members have the ability to propose different kind of ideas in order to increase the value delivery given by the Corner. For example, a member can propose an idea to do a new course for women based on “Basic computer knowledge for women entrepreneurs “. The YF members can make a proposal and submit the proposal to the American embassy and take the approval and he or she can proceed with the proposed idea.

      
        
          

